package wahlschwabe;

public class StringOccurance {
    private String word;
    private int occurance;

    StringOccurance(String word, int occurance){
        this.word = word;
        this.occurance = occurance;
    }

    public String getWord() {
        return this.word;
    }

    public int getOccurance() {
        return this.occurance;
    }
    
}

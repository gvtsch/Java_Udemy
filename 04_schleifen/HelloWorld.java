import java.util.Scanner;

public class HelloWorld {
    public static void main(String[] args) {
        int i = 0;
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    
        for (int j = 20; j >= 10; j--) {
            System.out.println(j);
        }
    }    
}

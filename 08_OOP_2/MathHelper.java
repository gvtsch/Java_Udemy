public class MathHelper {
    
    public int min(int a, int b) {
        if (a < b) {
            return a;
        } else {
            return b;
        }
    }

    public double min(double a, double b) {
        if (a < b) {
            return a;
        } else {
            return b;
        }
    }

    public static double min_static(double a, double b) {
        if (a < b) {
            return a;
        } else {
            return b;
        }
    }
}
